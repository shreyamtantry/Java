package com.sangamone;

import java.util.Scanner;

public class multiples {

	
	    public static void main(String[] args) {
	        int N = 2;

	        for (int i = 1; i <= 10; i++) {
	            int result = N * i;
	            System.out.printf("%d x %d = %d\n", N, i, result);
	        }
	    }
	}
